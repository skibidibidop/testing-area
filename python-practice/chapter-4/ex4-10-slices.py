# Make a list
# Use slice to print "The first three items in this list are: "
# Use slice to print "Three items from the middle of the list are: "
# Use slice to print "The last three items in this list are: "

meals = ["pizza", "pasta", "burger", "fried chicken", "caesar salad"]

print(f"The first three items in this list are: {meals[0:3]}")
print(f"Three items from the middle of the list are : {meals[1:4]}")
print(f"The last three items in this list are: {meals[-3:]}")
